package ru.netology;

import org.junit.jupiter.api.Test;

import static io.restassured.RestAssured.given;
import static org.hamcrest.Matchers.*;

public class RestTest {
    @Test
    public void shouldCheckDataValue() {
        given()
                .baseUri("https://postman-echo.com")
                .body("some data")
                .when()
                .post("/post")
                .then()
                .statusCode(200)
                .body("data", is("false"));
    }

    @Test
    public void shouldCheckHostValue() {
        given()
                .baseUri("https://postman-echo.com")
                .body("some data")
                .when()
                .post("/post")
                .then()
                .statusCode(200)
                .body("headers.host", is("postman-echo.com"));
    }

    @Test
    public void shouldCheckJsonIsNull() {
        given()
                .baseUri("https://postman-echo.com")
                .body("some data")
                .when()
                .post("/post")
                .then()
                .statusCode(200)
                .body("json", is(nullValue()));
    }

    @Test
    public void shouldCheckFilesIsEmpty() {
        given()
                .baseUri("https://postman-echo.com")
                .body("some data")
                .when()
                .post("/post")
                .then()
                .statusCode(200)
                .body("files", is(anEmptyMap()));
    }
}